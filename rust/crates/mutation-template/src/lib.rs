//! Mutation template — example/reference implementation.
//!
//! Each compiled mutation exports a `brain_mutation_vtable` symbol.
//! This template shows how to implement a Hebbian update mutation.

use brain_traits::*;

/// Example mutation: absolute top-k sparsification of the delta.
///
/// This replaces the standard Hebbian update with one that only keeps
/// the top-k largest absolute values in the delta matrix.
unsafe extern "C" fn hebbian_update_abs_topk(
    _state: *mut HebbianState,
    _batch: *const BatchPair,
) -> HebbianUpdateResult {
    // This is a template — real mutations would be generated by the LLM
    // and compiled against brain-traits.
    HebbianUpdateResult { status: 0 }
}

/// The vtable that the experiment runner loads.
#[unsafe(no_mangle)]
pub static brain_mutation_vtable: MutationVtable = MutationVtable {
    version: ABI_VERSION,
    kind: MutationKind::HebbianUpdate,
    hebbian_update: Some(hebbian_update_abs_topk),
    hebbian_prune: None,
    sparse_forward: None,
    temporal_update: None,
};
